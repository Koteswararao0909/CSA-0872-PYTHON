# Define a function to return the maximum of three numbers
def max_of_three(num1, num2, num3):
    # Use the max() function to find the maximum among the three numbers
    maximum = max(num1, num2, num3)
    return maximum

# Example usage:
result = max_of_three(10, 5, 8)
print("The maximum of the three numbers is:", result)

